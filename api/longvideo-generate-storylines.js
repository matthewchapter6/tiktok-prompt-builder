// longvideo-generate-storylines.js
// Gemini Flash — generates 5 host-centric 18s video storylines (3-act: Hook/Content/CTA)
// Product image used as visual context only — not embedded in video after clip 1

export default async function handler(req, res) {
  res.setHeader('Access-Control-Allow-Origin', '*');
  res.setHeader('Access-Control-Allow-Methods', 'POST, OPTIONS');
  res.setHeader('Access-Control-Allow-Headers', 'Content-Type');
  if (req.method === 'OPTIONS') return res.status(200).end();
  if (req.method !== 'POST') return res.status(405).json({ error: 'Method not allowed' });

  try {
    const {
      productDescription,  // required
      productUSP,          // optional
      funnel,              // 'upper' | 'middle' | 'lower'
      shape,               // optional: cube | rectangular | sphere | cylinder | cone | pyramid | other
      dimensions,          // optional: { height, width, depth } in cm
      productImage,        // optional: { data: base64, mimeType }
      lang,                // 'en' | 'zh' | 'bm'
    } = req.body;

    if (!productDescription) {
      return res.status(400).json({ error: 'productDescription is required' });
    }

    const funnelGuide = {
      upper:  'AWARENESS — hook with a relatable problem, no hard sell, make viewer curious',
      middle: 'CONSIDERATION — show product solving a real problem, build trust and credibility',
      lower:  'CONVERSION — create urgency, strong desire, clear reason to buy NOW',
    }[funnel] || 'GENERAL — showcase product attractively with a clear benefit';

    const dimParts = [];
    if (dimensions?.height) dimParts.push(`${dimensions.height}cm tall`);
    if (dimensions?.width)  dimParts.push(`${dimensions.width}cm wide`);
    if (dimensions?.depth)  dimParts.push(`${dimensions.depth}cm deep`);
    const shapeContext = shape
      ? `PRODUCT SHAPE & SIZE: ${shape}${dimParts.length ? ` — approximately ${dimParts.join(', ')}` : ''} — ensure the host holds and interacts with the product in a physically realistic way (correct hand grip, natural size proportions, no floating or oversized product).`
      : '';

    const langInstruction = lang === 'zh'
      ? '\n\nIMPORTANT: Generate ALL text fields (title, hook, content, cta, host, scene, emotion, style) in Simplified Chinese (简体中文).'
      : lang === 'bm'
      ? '\n\nIMPORTANT: Generate ALL text fields (title, hook, content, cta, host, scene, emotion, style) in Bahasa Malaysia.'
      : '';

    const systemInstruction = `You are a senior social media content producer specialising in 18-second host-driven product marketing videos for TikTok and Instagram Reels.

You create compelling 3-act video stories where a single consistent human host introduces and showcases a product.

HOST-CENTRIC FRAMEWORK (non-negotiable):
- ONE consistent host carries the entire 18-second video
- The host holds and physically interacts with the product in Acts 1 and 2
- The same scene/location is maintained throughout — no jump cuts to different locations
- No cast changes — the same character from start to finish

CRITICAL PRODUCT VISIBILITY RULE:
- The product reference image is only used in Act 1 (clip 1 generation)
- Acts 2 and 3 are generated by AI video extension — they inherit visual continuity from Act 1
- Therefore: the story MUST keep the product visible in Acts 1 AND 2 (host holds/interacts with it)
- For Act 3 (CTA): host can set product down on a surface, gesture toward it, or continue holding it
- NEVER design a story where the product disappears after Act 1 or where a completely new scene appears

Story arc: Hook (Act 1, 0-6s) → Content/Demo (Act 2, 6-12s) → CTA (Act 3, 12-18s)

Return ONLY valid JSON. No explanation. No markdown. No preamble.${langInstruction}`;

    const userPrompt = `Propose 5 completely different 18-second host-driven product video storylines.

PRODUCT: ${productDescription}
${productUSP ? `USP: ${productUSP}` : ''}
${shapeContext}
OBJECTIVE: ${funnelGuide}
DURATION: 18 seconds = Act 1 Hook (0-6s) + Act 2 Content (6-12s) + Act 3 CTA (12-18s)

RULES:
- Each storyline must have a completely different emotional hook and energy
- Each must feel like a different type of ad: lifestyle, demo, testimonial, cinematic, UGC
- Host appearance (gender, age range, look, energy) — choose based on what suits this product and funnel
- The host must physically hold and interact with the product in Acts 1 and 2
- Product stays in frame throughout Acts 1 and 2 — host keeps holding it or interacts with it
- Act 3: host delivers a strong CTA — product remains visible or clearly placed on a surface in shot
- Suggest a specific location/environment that suits the product

Return exactly this JSON:
{
  "storylines": [
    {
      "id": 1,
      "title": "3-5 word catchy title",
      "hook": "What happens in Act 1 (0-6s): host appearance, opening action, how host picks up/introduces the product",
      "content": "What happens in Act 2 (6-12s): product demo or feature shown by host while still holding it",
      "cta": "What happens in Act 3 (12-18s): host CTA delivery, closing shot — product still visible",
      "host": "Brief host description: gender, age range, look/energy that suits this product and funnel",
      "scene": "Location/environment for the whole video — must stay consistent across all 3 acts",
      "emotion": "Primary emotion targeted",
      "style": "e.g. UGC, cinematic, lifestyle, testimonial"
    }
  ]
}`;

    const parts = [];

    if (productImage?.data) {
      parts.push({
        inline_data: { mime_type: productImage.mimeType || 'image/jpeg', data: productImage.data }
      });
      parts.push({ text: `This is the product reference image. Use it to understand the product's appearance, colour, and real-world size so host interactions are physically realistic.` });
    }

    parts.push({ text: userPrompt });

    const response = await fetch(
      `https://generativelanguage.googleapis.com/v1beta/models/gemini-2.0-flash:generateContent?key=${process.env.GOOGLE_API_KEY}`,
      {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({
          system_instruction: { parts: [{ text: systemInstruction }] },
          contents: [{ role: 'user', parts }],
          generationConfig: {
            temperature: 1.2,
            topP: 0.97,
            maxOutputTokens: 2000,
          },
        }),
      }
    );

    const data = await response.json();
    if (!response.ok) {
      console.error('[longvideo-generate-storylines] Gemini error:', JSON.stringify(data));
      return res.status(response.status).json({ error: 'Storyline generation failed', details: data });
    }

    const raw = data?.candidates?.[0]?.content?.parts?.[0]?.text?.trim() || '';
    if (!raw) return res.status(500).json({ error: 'Empty response from Gemini' });

    let parsed;
    try {
      parsed = JSON.parse(raw.replace(/```json|```/g, '').trim());
    } catch (e) {
      console.error('[longvideo-generate-storylines] Parse error:', e.message);
      return res.status(500).json({ error: 'Failed to parse storylines', raw });
    }

    console.log(`[longvideo-generate-storylines] funnel=${funnel} stories=${parsed.storylines?.length}`);
    return res.status(200).json(parsed);

  } catch (error) {
    console.error('[longvideo-generate-storylines] Exception:', error.message);
    return res.status(500).json({ error: 'Storyline generation failed', details: error.message });
  }
}
